package com.commonsware.cwac.cam2.model;

public class RectangleModel {
  public float left;
  public float top;
  public float right;
  public float bottom;

  public RectangleModel(float left, float top, float right, float bottom) {
    this.left = left;
    this.top = top;
    this.right = right;
    this.bottom = bottom;
  }
}
